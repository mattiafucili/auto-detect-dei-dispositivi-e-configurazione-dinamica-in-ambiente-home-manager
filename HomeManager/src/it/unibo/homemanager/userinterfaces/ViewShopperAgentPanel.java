    private void saveThresholdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveThresholdButtonActionPerformed
       int choice = JOptionPane.showConfirmDialog(null,
                "Do you really want to insert this threshold value?",
                "INSERT THRESHOLD CHECK", JOptionPane.OK_CANCEL_OPTION);
        if (choice == JOptionPane.OK_OPTION) {
            int threshold=Integer.parseInt(this.thresholdTextField.getText());
            this.shopperAgent.insert(threshold);
            return;
        } else {
            JOptionPane.showMessageDialog(null,
                    "Threshold not added,try again.",
                    "ACTION NOT SUCCEEDE", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_saveThresholdButtonActionPerformed

    private void yourOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yourOrderButtonActionPerformed
        ArrayList<String> orders=shopperAgent.getListOrderToString();
        if(orders.size()!=0){
            viewYourOrder.refreshList(orders);
            //viewYourOrder.fiillTextArea(lista);
            viewYourOrder.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "There are no orders, empty cart.",
                    "EMPTY CART", JOptionPane.NO_OPTION);
        }    
    }//GEN-LAST:event_yourOrderButtonActionPerformed

    private void plusThresholdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plusThresholdButtonActionPerformed
        int num_threshold=Integer.parseInt(this.thresholdTextField.getText());
        num_threshold++;
        this.thresholdTextField.setText(String.valueOf(num_threshold));
    }//GEN-LAST:event_plusThresholdButtonActionPerformed

    private void lessThresholdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lessThresholdButtonActionPerformed
        int num_threshold=Integer.parseInt(this.thresholdTextField.getText());
        if(num_threshold>2){
            num_threshold--;
            this.thresholdTextField.setText(String.valueOf(num_threshold));
        }else{
            JOptionPane.showMessageDialog(null,
                    "Threshold too low, action not permitted",
                    "ACTION NOT PERMITTED", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_lessThresholdButtonActionPerformed


